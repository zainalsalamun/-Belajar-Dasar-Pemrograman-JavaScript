function tampilkanHari(angka) {
    switch (angka) {
        case 1:
            console.log("Minggu");
            break;
        case 2:
            console.log("Senin");
            break;
        case 3:
            console.log("Selasa");
            break;
        case 4:
            console.log("Rabu");
            break;
        case 5:
            console.log("Kamis");
            break;
        case 6:
            console.log("Jumat");
            break;
        case 7:
            console.log("Sabtu");
            break;
        default:
            console.log("Tidak Tahu");
    }
}


tampilkanHari(3);
tampilkanHari(6); 
tampilkanHari(8);
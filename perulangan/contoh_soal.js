// Perulangan menggunakan while
let i = 1;
while (i <= 20) {
  if (i % 2 !== 0) {
    console.log(i);
  }
  i++;
}

// Perulangan menggunakan do-while
let j = 1;
do {
  if (j % 2 !== 0) {
    console.log(j);
  }
  j++;
} while (j <= 20);

// Perulangan menggunakan for
for (let k = 1; k <= 20; k++) {
  if (k % 2 !== 0) {
    console.log(k);
  }
}
